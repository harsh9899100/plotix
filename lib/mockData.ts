export const formatPrice = (price: number, listingFor: string): string => {
  if (listingFor === 'rent') {
    return `₹${price.toLocaleString('en-IN')}/mo`
  }
  if (price >= 10000000) {
    return `₹${(price / 10000000).toFixed(2)} Cr`
  }
  if (price >= 100000) {
    return `₹${(price / 100000).toFixed(1)} L`
  }
  return `₹${price.toLocaleString('en-IN')}`
}

